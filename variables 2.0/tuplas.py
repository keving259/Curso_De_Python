'''
Las tuplas se suelen crear para solo lectura de datos (no escritura), y están más optimizadas que las listas
'''

#creando tuplas con tuple()
tupla = tuple(["dato1","dato2"]) # Convierte una lista en una tupla

#creando una tupla sin parentesis de multiples datos
tupla = "dato1","dato2"
# Es lo mismo que esto:
tupla = ("dato1","dato2")

#creando una tupla sin paréntesis de un solo dato
tupla = "dato", # Se redefine


print(tupla)
